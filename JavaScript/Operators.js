//Asignment Operators
let x = 5;

//Arithmetic Operators
let y = 10;
console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

//Relational Operators
console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);
console.log(x == y);
console.log(x != y);

//Logical Operators
console.log(x > 5 && y > 5);
console.log(x > 5 || y > 5);

//Bitwise Operators
console.log(x & y);
console.log(x | y);
console.log(x ^ y);
console.log(~x);
console.log(x << y);
console.log(x >> y);

//Ternary Operators
let z = x > y ? x : y;
console.log(z);
